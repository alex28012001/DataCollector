namespace DataCollector.Models.Entities
{
    /// <summary>
    /// The class contains user contacts.
    /// </summary>
    public class Contacts
    {
        /// <summary>
        /// Contains the mobile phone.
        /// </summary>
        public string MobilePhone { get; set; }

        /// <summary>
        /// Contains the email.
        /// </summary>
        public string Email { get; set; } 
        
        /// <summary>
        /// Contains the skype.
        /// </summary>
        public string Skype { get; set; }

        /// <summary>
        /// Contains the vk.
        /// </summary>
        public string Vk { get; set; }

        /// <summary>
        /// Contains the facebook.
        /// </summary>
        public string Facebook { get; set; }

        /// <summary>
        /// Contains the instagram.
        /// </summary>
        public string Instagram { get; set; }

        /// <summary>
        /// Contains the twitter.
        /// </summary>
        public string Twitter { get; set; }
    }
}

