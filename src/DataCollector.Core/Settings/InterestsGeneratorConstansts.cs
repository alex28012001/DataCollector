namespace DataCollector.Core.Settings
{
    /// <summary>
    /// Contains Constants for generating interests.
    /// </summary>
    public class InterestsGeneratorConstansts
    {
        /// <summary>
        /// Contains url template for searching items.
        /// </summary>
        public string SearchItemsUrlTemplate { get; set; }

        /// <summary>
        /// Contains url template for searching item.
        /// </summary>
        public string SearchItemUrlTemplate { get; set; }

        /// <summary>
        /// Contains title of books category.
        /// </summary>
        public string BooksCategory { get; set; }

        /// <summary>
        /// Contains title of films category.
        /// </summary>
        public string FilmsCategory { get; set; }

        /// <summary>
        /// Contains title of games category.
        /// </summary>
        public string GamesCategory { get; set; }

        /// <summary>
        /// Contains title of music category.
        /// </summary>
        public string MusicCategory { get; set; }
    }
}
